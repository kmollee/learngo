package main

import "fmt"

func main() {
	fmt.Println("Hello World")

	// declar variable
	var age int = 40
	var favNum float64 = 1.6180339
	fmt.Println(age, favNum)

	var numOne = 1.000
	var num99 = .9999
	fmt.Println(numOne + num99)

	fmt.Println("6 + 4 = ", 6+4)
	fmt.Println("6 - 4 = ", 6-4)
	fmt.Println("6 * 4 = ", 6*4)
	fmt.Println("6 / 4 = ", 6/4)
	fmt.Println("6 % 4 = ", 6%4)
}
